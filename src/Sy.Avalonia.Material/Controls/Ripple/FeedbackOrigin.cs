namespace Sy.Avalonia.Material.Controls;

/// <summary>
/// Defines the origin points for ripple feedback.
/// </summary>
public enum FeedbackOrigin {
	/// <summary>
	/// The feedback originates from the center of the control.
	/// </summary>
	Center,

	/// <summary>
	/// The feedback originates from the cursor position.
	/// </summary>
	Cursor,
}
