namespace Sy.Avalonia.Material.Media;

/// <summary>
/// Defines the case of characters in a control.
/// </summary>
public enum Casing {
	/// <summary>
	/// Leaves all characters unchanged.
	/// </summary>
	Normal,

	/// <summary>
	/// Converts all characters to lowercase.
	/// </summary>
	Lower,

	/// <summary>
	/// Converts all characters to uppercase.
	/// </summary>
	Upper,
}
